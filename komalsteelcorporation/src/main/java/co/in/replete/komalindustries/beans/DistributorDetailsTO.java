package co.in.replete.komalindustries.beans;

public class DistributorDetailsTO {

	private String distributorTrackId;
	
	private String firstName;
	
	private String lastName;
	
	private String displayName;
	
	public DistributorDetailsTO() {}

	public String getDistributorTrackId() {
		return distributorTrackId;
	}

	public void setDistributorTrackId(String distributorTrackId) {
		this.distributorTrackId = distributorTrackId;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	
}
